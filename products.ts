export const searchProducts = (query: string): Product[] => {
  const q = query.toLowerCase().trim();
  
  // If query is empty, return all products
  if (!q || q.length < 1) {
    return LICIOUS_PRODUCTS;
  }
  
  return LICIOUS_PRODUCTS.filter(p => {
    const name = p.name.toLowerCase();
    const desc = p.description.toLowerCase();
    
    // Check if any part of the query matches
    const queryParts = q.split(' ').filter(part => part.length > 0);
    return queryParts.some(part => name.includes(part) || desc.includes(part));
  });
};

// Calculate similarity score between query and product name (0-100)
const calculateSimilarity = (query: string, productName: string): number => {
  const q = query.toLowerCase().trim();
  const p = productName.toLowerCase();
  
  // Exact match
  if (q === p) return 100;
  
  // Name contains query exactly
  if (p.includes(q)) return 90;
  
  // Query contains significant parts of product name
  const queryWords = q.split(' ').filter(w => w.length > 2);
  const productWords = p.split(' ').filter(w => w.length > 2);
  
  let matchedWords = 0;
  for (const qWord of queryWords) {
    if (productWords.some(pWord => pWord.includes(qWord) || qWord.includes(pWord))) {
      matchedWords++;
    }
  }
  
  if (matchedWords === 0) return 0;
  const wordMatchScore = (matchedWords / Math.max(queryWords.length, productWords.length)) * 80;
  
  return wordMatchScore;
};

// Search for a specific product with high confidence match
export const findSpecificProduct = (query: string): Product | null => {
  const q = query.toLowerCase().trim();
  
  if (!q || q.length < 2) return null;
  
  let bestMatch: { product: Product; score: number } | null = null;
  
  for (const product of LICIOUS_PRODUCTS) {
    const score = calculateSimilarity(q, product.name);
    if (score > 0 && (!bestMatch || score > bestMatch.score)) {
      bestMatch = { product, score };
    }
  }
  
  // Return product only if confidence is high (>65%)
  return bestMatch && bestMatch.score > 65 ? bestMatch.product : null;
};